public interface CalculatorIF  {
    /**
     * Given two integers m and n, the function returns the result of m/n
     */
    int divide(int m, int n);
    /**
     * Given two integers m and n, the function returns the result of m*n
     */
    int multiplication(int m, int n);
    /**
     * Given two integers m and n, the function returns the result of m+n.
     */
    int sum(int m, int n);
}